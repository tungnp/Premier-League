#pragma mark - Table view delegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    PlayerDetailViewController* playerDetailViewController = [[PlayerDetailViewController alloc]initWithPlayer:[self.players objectAtIndex:indexPath.row]];
    [self.navigationController pushViewController:playerDetailViewController animated:YES];
